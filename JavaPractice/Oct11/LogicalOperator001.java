package JavaPractice.Oct11;

public class LogicalOperator001 {
    public static void main(String[] args) {
        // &&    AND
        // ||    OR
        //  !    NOT
        boolean a = false;
        boolean b = false;

        System.out.println(a || b);
        System.out.println(a && b);
        System.out.println(!a);



    }
}
